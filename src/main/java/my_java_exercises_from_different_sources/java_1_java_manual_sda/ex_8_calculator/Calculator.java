package my_java_exercises_from_different_sources.java_1_java_manual_sda.ex_8_calculator;

public class Calculator {

    public int add(int first, int second) {
        return first + second;
    }

    public int sub(int first, int second) {
        return first - second;
    }

    public int mul(int first, int second) {
        return first * second;
    }

    public int div(int first, int second) {
        return first / second;
    }

    public int mod(int first, int second) {
        return first % second;
    }

    public double mod(double first, double second) {
        return first / second;
    }


}
