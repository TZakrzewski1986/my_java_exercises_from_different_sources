public class Calculator {

    public int add(int first, int second) {
        return first + second;
    }

    public int sub(int first, int second) {
        return first - second;
    }

    public int mul(int first, int second) {
        return first * second;
    }

    public int div(int first, int second) {
        return first / second;
    }

    public int mod(int first, int second) {
        return first % second;
    }

    public double mod(double first, double second) {
        return first / second;
    }


}
